# get an input and test whether is divisible by 2, 3 and 5
# % is use as divide as well.

num = input("Type your number")

if int(num) % 2 == 0:
    print("Your number is divisible by 2.")

if int(num) % 3 == 0:
    print("Your number is divisible by 3")

if int(num) % 5 == 0:
    print("Your number is divisible by 5")


